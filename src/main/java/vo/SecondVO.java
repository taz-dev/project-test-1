package vo;

public class SecondVO {
	private int first_idx;
	private int second_idx;
	private String second_name;
	
	public int getFirst_idx() {
		return first_idx;
	}
	public void setFirst_idx(int first_idx) {
		this.first_idx = first_idx;
	}
	public int getSecond_idx() {
		return second_idx;
	}
	public void setSecond_idx(int second_idx) {
		this.second_idx = second_idx;
	}
	public String getSecond_name() {
		return second_name;
	}
	public void setSecond_name(String second_name) {
		this.second_name = second_name;
	}
	
	
	
}
